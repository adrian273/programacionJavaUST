    public void llenarDatoProducto() {
        int numero, numero2;
        String[] nombreProducto = {
            "Notebook", "Telefonos", "Lechugas", "Manzanas", "Consolas",
            "Televisores", "Equipos Musicales", "Tomates", "Peras"
        };
        String[] familiaProducto = {
            "LICOR",
            "VEGETALES",
            "FRUTAS",
            "TECNOLOGIA"
        };
        for (int i = 0; i < 30; i++) {
            numero = (int) (Math.random() * nombreProducto.length);
            numero2 = (int) (Math.random() * familiaProducto.length);

            String nombre = nombreProducto[numero];
            String famPro = familiaProducto[numero2];
            FamiliaProducto fp = FamiliaProducto.valueOf(famPro);
            String codigo = nombre + "" + i;
            int costo = (int) (Math.random() * 150000);
            Productos p = new Productos(nombre, fp, codigo, costo);
            dataProducto.add(p);
        }
    }

