    public void llenarTabla() {
        Object[] title = {
            "Nombre", "Familia", "Codigo", "Precio"
        };
        Object[] data = new Object[4];
        dm = new  DefaultTableModel(title, 0);
        for(int i = 0; i < Principal.dataProducto.size(); i++) {
            data[0] = Principal.dataProducto.get(i).getNombre();
            data[1] = Principal.dataProducto.get(i).getFp().name();
            data[2] = Principal.dataProducto.get(i).getCodigo();
            data[3] = Principal.dataProducto.get(i).getCosto();
            dm.addRow(data);
        }
       this.jTable1.setModel(dm);
    }

