    public void loadDataComboBox() {
        this.jcGenero.removeAllItems();
        this.jcGenero.addItem("");
        Genero[] tipoGenero = Genero.values();
        for(Genero g: tipoGenero)
            this.jcGenero.addItem(g.name());
    }
    
    public void addNewAuthor() {
        String name = this.jtNombre.getText();
        String dateBorn = this.jtFechaNacimiento.getText();
        String gender = (String) this.jcGenero.getSelectedItem();
        String resena = this.jtResena.getText();
        Genero g = Genero.valueOf(gender);
        Autor data = new Autor(g,resena, dateBorn, name);
        //p.setAutorData(this.dataAutor);
        Principal.autorData.add(data);
        this.jtNombre.setText("");
        this.jtFechaNacimiento.setText("");
        this.jtResena.setText("");
        this.jcGenero.setSelectedIndex(0);
    }
    
    
