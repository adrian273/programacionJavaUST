    /**
     * 
     * @param number numero ingresado del botton
     */
    public void addNumber(String number) {
        if (this.estado) {
            tfResultado.setText(number);
            this.lblOperacion.setText(this.operacion);
            this.estado = false;
        } else {
            tfResultado.setText(tfResultado.getText() + number);
        }
    }

    public void calculo(String op) {
        int result;
        switch (op) {
            case "suma":
                int numeroUno = Integer.parseInt(this.lblResultado.getText());
                int numeroDos = Integer.parseInt(this.tfResultado.getText());
                result = numeroUno + numeroDos;
                this.tfResultado.setText("");
                this.lblResultado.setText(String.valueOf(result));
                this.lblOperacion.setText(op);
                break;
            case "resta":
                int numeroRUno = Integer.parseInt(this.lblResultado.getText());
                int numeroRDos = Integer.parseInt(this.tfResultado.getText());
                if(numeroRUno > numeroRDos) {
                    result = numeroRUno - numeroRDos;
                } else {
                    result = numeroRDos - numeroRUno;
                }
                
                this.tfResultado.setText("");
                this.lblResultado.setText(String.valueOf(result));
                this.lblOperacion.setText(op);
                break;
                
        }
    }

