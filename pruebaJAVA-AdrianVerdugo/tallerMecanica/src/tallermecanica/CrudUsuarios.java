    public void loadDataUsuario() {
        String title[] = {"NÂ°", "Nombre", "Email", "Fecha Creacion"};
        dm = new DefaultTableModel(title, 0);
        try {
            ResultSet r = um.viewUser();
            if (r.next()) {
                r.beforeFirst();
                while (r.next()) {
                    String number = r.getString("idUsuario");
                    String nombre = r.getString("nombreUsuario");
                    String email = r.getString("EmailUsuario");
                    String fechCreacion = r.getString("fechaCreacionUsuario");
                    Object data[] = {number, nombre, email, fechCreacion};
                    dm.addRow(data);
                }
                this.jtDataUser.setModel(dm);
            } else {
                JOptionPane.showMessageDialog(null, "No hay Registros!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

