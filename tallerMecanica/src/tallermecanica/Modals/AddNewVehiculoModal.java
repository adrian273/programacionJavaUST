    /**
     * TODO Mostrar las marcas disponibles de los vehiculos
     *
     * @throws SQLException
     */
    public void loadComboBoxMarca() throws SQLException {
        ResultSet r = vm.viewMarca();
        try {
            if (r.next()) {
                r.beforeFirst();
                while (r.next()) {
                    this.jcMarca.addItem(r.getString("nombreMarca"));
                }
            } else {
                JOptionPane.showMessageDialog(null, "No hay marcas registradas", "Error", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

