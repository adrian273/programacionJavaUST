    /**
     * TODO cargas de datos del vehiculo
     * @exception e
     */
    public void loadDataVehiculo() {
        ResultSet data = this.rs;
        try {
            while(data.next()) {
                this.jlPatente.setText(data.getString("patenteVehiculo"));
                this.jlMarca.setText(data.getString("nombreMarca"));
                this.jlModelo.setText(data.getString("modelo"));
                this.jlColor.setText(data.getString("color"));
                this.jlYear.setText(data.getString("yearVehiculo"));
                this.jlFechaRegistro.setText(data.getString("fechaRegistroVehiculo"));
                this.jlRutCliente.setText(data.getString("clientes_rutCliente"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

