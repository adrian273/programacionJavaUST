    /**
     * TODO Agregar nuevo usuario al sistema
     * @param evt 
     */
    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        String nombre = this.jtNombre.getText();
        String email = this.jtEmail.getText();
        String password;
        password = this.jtPassword.getText();
        if (nombre.equals("")) {
            JOptionPane.showMessageDialog(null, "Nombre Requerido!", "Error", JOptionPane.ERROR_MESSAGE);
            this.jtNombre.requestFocus();
        } else if (email.equals("")) {
            JOptionPane.showMessageDialog(null, "Email Requerido", "Error", JOptionPane.ERROR_MESSAGE);
            this.jtEmail.requestFocus();
        } else if (password.equals("")) {
            JOptionPane.showMessageDialog(null, "Contraseña Requerida", "Error", JOptionPane.ERROR_MESSAGE);
            this.jtPassword.requestFocus();
        } else {
            try {
                um.addNewUser(nombre, email, password);
                JOptionPane.showMessageDialog(null, "TAgregado Con exito!", "Info", JOptionPane.INFORMATION_MESSAGE);
                this.jtNombre.setText("");
                this.jtEmail.setText("");
                this.jtPassword.setText("");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error Al ingresar Datos", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

